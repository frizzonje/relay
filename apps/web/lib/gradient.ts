/**
 * Детерминированные градиенты для иконок серверов и аватаров участников.
 *
 * Палитра намеренно узкая и приглушённая — только холодный диапазон
 * (сталь / индиго / бирюза / муар-фиолет / графит). Так набор читается как единое
 * целое поверх почти чёрных поверхностей relay, а не как случайная радуга.
 * Цвет стабилен для одного seed (id сервера / имя участника).
 */
const GRADIENTS: readonly (readonly [string, string])[] = [
  ['#3f5570', '#263341'], // сталь-синий
  ['#494d78', '#2b2d49'], // индиго
  ['#356661', '#1f3b38'], // бирюза
  ['#524d70', '#302c45'], // муар-фиолет
  ['#3a5e73', '#223743'], // океан
  ['#4a525d', '#2b2f36'], // графит
  ['#574a68', '#322a3e'], // сливовый
  ['#356170', '#203a42'], // глубокий циан
];

function hash(seed: string): number {
  let h = 0;
  for (let i = 0; i < seed.length; i++) h = (h * 31 + seed.charCodeAt(i)) >>> 0;
  return h;
}

/** Приглушённый косой градиент из холодной палитры для произвольного seed. */
export function seedGradient(seed: string): string {
  const [a, b] = GRADIENTS[hash(seed || '?') % GRADIENTS.length];
  return `linear-gradient(140deg, ${a}, ${b})`;
}
